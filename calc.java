package calc;


public class calc {
	double plus(double a, double b) {
		return a+b;
	}
	double minus(double a, double b) {
		return a-b;
	}
	double kobeitu(double a, double b) {
		return a*b;
	}
	double bolu(double a, double b) {
		return (double)a/b;
	}
	
	
	
	double stepen(double a, double b) {
		return Math.pow(a,b);
	}
	
	double C() {
		return 0;
	}
	
	
	
	
}

